package data;

/**
 * @author mk
 */
public class Position
{
    private int no;
    private String name;
    private int salary;

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public void setNo(int no)
    {
        this.no = no;
    }

    public int getNo()
    {
        return no;
    }

    public int getSalary()
    {
        return salary;
    }

    public void setSalary(int salary)
    {
        this.salary = salary;
    }
}
